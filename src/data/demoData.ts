  const q = query.toLowerCase();
  
  for (const [key, keywords] of Object.entries(stackKeywordMap)) {
    if (keywords.some(keyword => q.includes(keyword))) {
      return { ...STACK_PRESETS[key], isSample: true };
    }
  }
  
  // Default to SaaS Stack
